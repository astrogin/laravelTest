<?php

namespace App\Console\Commands;

use App\Services\PaymentSystems\Transaction\Transaction;
use Illuminate\Console\Command;

class StatusTransaction extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'check:transaction';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Transaction status';

    /**
     * Create a new command instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * Execute the console command.
     *
     * @return mixed
     */
    public function handle()
    {
        $transactionService = new Transaction();
        $transactionService->run();
    }
}
